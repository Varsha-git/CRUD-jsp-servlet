package model;

public class Result {

	private String rollno;
	private String name;
	private String fname;
	private String dbms;
	private String data_structure;
	private String descrete_maths;

	public String getRollno() {
		return rollno;
	}

	public void setRollno(String rollno) {
		this.rollno = rollno;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getFname() {
		return fname;
	}

	public void setFname(String fname) {
		this.fname = fname;
	}

	public String getDbms() {
		return dbms;
	}

	public void setDbms(String dbms2) {
		this.dbms = dbms2;
	}

	public String getData_structure() {
		return data_structure;
	}

	public void setData_structure(String ds) {
		this.data_structure = ds;
	}

	public String getDescrete_maths() {
		return descrete_maths;
	}

	public void setDescrete_maths(String maths) {
		this.descrete_maths = maths;
	}

}
